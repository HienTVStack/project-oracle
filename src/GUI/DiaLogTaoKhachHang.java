    private void btnTaoTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoTaiKhoanActionPerformed
        try {
            // TODO add your handling code here:
            if(txtHoVaTen.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Nhập họ và tên");
                return;
            }
            else if(txtSoDienThoai.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Nhập số điện thoại");
                return;  
            }
            else if(!kiemTraSo(txtSoDienThoai.getText())){
                JOptionPane.showMessageDialog(this, "Phải là số");
                return;
            }
            else if(!kiemTraTrungSDT()){
                JOptionPane.showMessageDialog(this, "Số điện thoại này đã được sử dụng");
                txtSoDienThoai.setText("");
                return;
            }
            else if(txtNamSinh.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Nhập năm sinh");
                return;                
            }
            else if(!kiemTraSo(txtNamSinh.getText())){
                JOptionPane.showMessageDialog(this, "Phải là số");
                return;
            }
            connection = DBconnection.getConnection(frm_DangNhap.tenDangNhap(), frm_DangNhap.matKhauDangNhap());
            String query = "INSERT INTO tbl_KhachHang(sMatKhau, sHoTen, iNamSinh, bGioiTinh, sSoDienThoai, sNgayDangKy, bQuyenDangNhap)"
                    + " VALUES (?, ?, ?, ?, ?, ?, ?)";
            preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, "123");
            preparedStatement.setString(2, txtHoVaTen.getText());
            preparedStatement.setString(3, txtNamSinh.getText());
            if(rbNam.getText().equals("Nam"))
                preparedStatement.setString(4, "1");
            else preparedStatement.setString(4, "0");
            preparedStatement.setString(5, txtSoDienThoai.getText());
            preparedStatement.setString(6, "26/7/2021");
            preparedStatement.setString(7, "1");
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Tạo thành công");
            txtHoVaTen.setText("");
            txtNamSinh.setText("");
            txtSoDienThoai.setText("");
            this.setVisible(false);
        } catch (SQLException ex) {
           // JOptionPane.showMessageDialog(this, "Có lỗi");
           ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnTaoTaiKhoanActionPerformed
    
