    public boolean kiemTraTrungSDT(){
        boolean kq = false;
        try {            
            connection = DBconnection.getConnection(frm_DangNhap.tenDangNhap(), frm_DangNhap.matKhauDangNhap());
            String query = "SELECT sSoDienThoai FROM tbl_TaiKhoan WHERE sSoDienThoai = '"+txtSoDienThoai.getText()+"'";
            preparedStatement = connection.prepareStatement(query);
            resultset = preparedStatement.executeQuery();
            if(!resultset.next())
                kq = true;            
        } catch (SQLException ex) {
            Logger.getLogger(DiaLogTaoKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        return kq;
    }
