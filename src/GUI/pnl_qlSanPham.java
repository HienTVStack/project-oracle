    private void showData(){
        try {
            jTableDanhSachSanPham.removeAll();
            String[] arr = {"Mã sản phẩm", "Tên sản phẩm", "Giá bán", "Xuất xứ", "SL tồn", "Loại SP", "Còn hàng", "Hiển thị", "Bán chạy", "SL xem"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            
            Connection connection = DBconnection.getConnection(frm_DangNhap.tenDangNhap(), frm_DangNhap.matKhauDangNhap());
            
            String query = "SELECT * FROM dbo.[tbl_SanPham]";
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("PK_iSanPhamID"));
                vector.add(rs.getString("sTenSanPham"));
                vector.add(rs.getString("fGiaBan"));
                vector.add(rs.getString("sXuatXu"));
                vector.add(rs.getInt("iSoLuongTon"));
                vector.add(rs.getString("FK_sDanhMucSanPhamID"));
                vector.add(rs.getBoolean("bConHang"));
                vector.add(rs.getBoolean("bHienThi"));
                vector.add(rs.getBoolean("bBanChay"));
                vector.add(rs.getInt("iSoLuongXem"));
                
                model.addRow(vector);
                
            }
            jTableDanhSachSanPham.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(jframl_qlSanPham.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Load dữ liệu vào combobox
        try {
            ComboBoxLoaiSanPham.removeAll();
            String query = "SELECT sTenNhomSanPham FROM tbl_NhomSanPham";
            Connection connection = DBconnection.getConnection();
            preparedStatement = connection.prepareStatement(query);
            resultset = preparedStatement.executeQuery();            
            while(resultset.next()){
                
                cbxNhomSanPham.addItem(resultset.getString("sTenNhomSanPham"));
                
            }
            resultset.close();
            preparedStatement.close();
            connection.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
       
    }
