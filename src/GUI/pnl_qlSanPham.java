    private void jTableDanhSachSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDanhSachSanPhamMouseClicked
       
            // TODO add your handling code here:
            int i = jTableDanhSachSanPham.getSelectedRow();
//
            TableModel model = jTableDanhSachSanPham.getModel();

        txtMaSanPham.setText(model.getValueAt(i, 0).toString());
        txtTenSanPham.setText(model.getValueAt(i, 1).toString());
        txtGiaBan.setText(model.getValueAt(i, 2).toString());
        txtXuatXu.setText(model.getValueAt(i, 3).toString());
        txtSoLuongCon.setText(model.getValueAt(i, 4).toString());

        String conHang = model.getValueAt(i, 6).toString();
            switch(conHang){
                case "true":
                    jRadioButtonONConHang.setSelected(true);
                    jRadioButtonOFFBanChay.setSelected(false);
                    break;
                default:
                    jRadioButtonONConHang.setSelected(false);
                    jRadioButtonOFFBanChay.setSelected(true);
                    break;
            }
        String hienThi = model.getValueAt(i, 7).toString();
            switch(hienThi){
                case "true":
                    jRadioButtonOFFHienThi.setSelected(false);
                    RadioButtonOnHienThi.setSelected(true);
                    break;
                default:
                    jRadioButtonOFFHienThi.setSelected(true);
                    RadioButtonOnHienThi.setSelected(false);
                    break;
            }
        String banChay = model.getValueAt(i, 8).toString();
            switch(banChay){
                case "true":
                    jRadioButtonONBanChay.setSelected(true);
                    jRadioButtonOFFConHang.setSelected(false);
                    break;
                default:
                    jRadioButtonONBanChay.setSelected(false);
                    jRadioButtonOFFConHang.setSelected(true);
                    break;
            }
        txtSoLuongXem.setText(model.getValueAt(i, 9).toString());
        
        this.btnThem.setEnabled(false);
        this.btnXoa.setEnabled(true);
          
        
    }//GEN-LAST:event_jTableDanhSachSanPhamMouseClicked

